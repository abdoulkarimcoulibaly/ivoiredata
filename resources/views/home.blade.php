                        La Côte d’Ivoire, en forme longue république de Côte d’Ivoire (RCI), plus rarement désignée Terre d’Éburnie,
                        est un État situé en Afrique, sur l'océan Atlantique, dans la partie occidentale du golfe de Guinée.
                        D’une superficie de 322 462 km², elle est bordée au nord-nord-ouest par le Mali, au nord-est par le Burkina Faso,
                        à l'est par le Ghana, au sud-ouest par le Liberia, au nord-ouest par la Guinée et au sud par l’océan Atlantique.
                        La population est estimée à 26 378 274 habitants en 2020.
                        La Côte d’Ivoire a pour <strong>capitale politique et administrative Yamoussoukro</strong> bien que la
                        quasi-totalité des institutions se trouvent à Abidjan, son principal centre économique.
                        Ayant pour langue officielle le français, il existe quelque 70 langues et dialectes parlés au quotidien.
                        Elle a pour monnaie, le franc CFA. Le pays fait partie de la CEDEAO, de l’Union africaine et de l'Organisation de la coopération islamique.
                        D’abord protectorat français en 1843 et devenu colonie française le 10 mars 1893, le pays acquiert son indépendance le 7 août 1960,
                        sous la houlette de <strong>Félix Houphouët-Boigny, premier président de la République</strong>.
                        L’économie, essentiellement axée sur la production de café et de cacao, connaît au cours des deux premières décennies un essor exceptionnel,
                        faisant de la Côte d’Ivoire un pays phare en Afrique de l'Ouest. En 1990, le pays traverse, outre la crise économique survenue à la fin des années 1970,
                        des périodes de turbulence sur les plans social et politique. Ces problèmes connaissent une exacerbation à la mort de Félix Houphouët-Boigny en 1993.
                        L’adoption d’une nouvelle constitution9 et l’organisation de l’élection présidentielle qui, en 2000, porte au pouvoir Laurent Gbagbo,
                        n’apaisent pas les tensions sociales et politiques, qui conduisent au déclenchement d’une crise politico-militaire le 19 septembre 2002.
                        Après plusieurs accords de paix, l'élection présidentielle de 2010 voit la victoire d'Alassane Ouattara face à son opposant Laurent Gbagbo.
                        Réélu en 2015, Alassane Ouattara relance la croissance économique par une politique libérale et interventionniste tout en étant critiqué pour sa gestion de l'armée et de la justice.
                        En 2016, une nouvelle constitution est adoptée, initiant la Troisième République.
                        Cette nouvelle constitution a subi des modifications le 17 mars 2020.
                        La Côte d’Ivoire est en voie de développement et se place en 165e position selon son indice de développement humain (IDH) en 2018.
